-- get a hex list of colors based on current bg and constrast config
local function get_colors()
  local p = Base16Eighties.palette
  local config = Base16Eighties.config

  for color, hex in pairs(config.palette_overrides) do
    p[color] = hex
  end

  return {
    bg0 = p.dark0,
    bg1 = p.dark1,
    bg2 = p.dark2,
    bg3 = p.dark3,
    bg4 = p.dark4,
    fg0 = p.light0,
    fg1 = p.light1,
    fg2 = p.light2,
    fg3 = p.light3,
    fg4 = p.light4,
    red = p.red,
    green = p.green,
    yellow = p.yellow,
    blue = p.blue,
    purple = p.purple,
    aqua = p.aqua,
    orange = p.orange,
    gray = p.gray,
  }
end

